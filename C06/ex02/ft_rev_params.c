#include <unistd.h>

int	main(int argc, char *argv[])
{
	int	i;
	int	n;

	i = argc - 1;
	while (i > 0)
	{
		n = 0;
		while (argv[i][n] != '\0')
		{
			write (1, &argv[i][n], 1);
			n++;
		}
		write (1, "\n", 1);
		i--;
	}
	return (0);
}
