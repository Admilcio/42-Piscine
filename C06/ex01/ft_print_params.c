#include <unistd.h>

int	main(int argc, char *argv[])
{
	int	i;
	int	n;

	i = 1;
	n = 0;
	while (i < argc)
	{
		n = 0;
		while (argv[i][n] != '\0')
		{
			write (1, &argv[i][n], 1);
			n++;
		}
		i++;
		write (1, "\n", 1);
	}
	return (0);
}
